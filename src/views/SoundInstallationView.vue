    <p>
      For Three Systems: On Hold, I was inspired to explore perceptual alertness
      and its suspension— the way listening can shift between moments of
      anticipation and immersion. I wanted the installation to be a space where
      these states coexisted, where one’s experience might oscillate between
      them. While seeking a way to approach this, I was motivated to cultivate a
      moment where the independent perspectives of Sophie, Jo and I could
      function as parallel lines that left room for the possibility of overlap
      and exchange – not only conceptually, in our idiosyncratic approaches to
      sound, materials and meaning-making, but also in how these could interact
      in the installation
    </p>
    <p>
      Our process was iterative: we moved between saturation (intense
      discussion, idea-sharing), reflection (time apart to process and create),
      and re-saturation (returning with new perspectives, sounds, materials).
      Over time, what began as disparate fragments of ideas gradually converged…
    </p>
    <p>
      My contribution to the installation involved creating a system where a
      speaker and microphone generated feedback in response to the space. Using
      a SuperCollider patch, I analyzed the sounds within the room—particularly
      the other components of the installation—to determine when and for how
      long a feedback loop would persist. I was interested in how the present
      material conditions of the space (such as the vibrating wooden stick)
      could be connected to the abstract, non-material elements (like the hold
      music recording).
    </p>
    <p>
      At times, the interplay between these elements—feedback swelling to a
      threshold before dissipating, wood rattling intermittently as a voice
      counts numbers that don’t seem to be quite right, moments of near silence
      stretching just long enough for the room to shift..
    </p>
