    <h2>Sophie: waiting</h2>
    <p class="subheading">Listen to text</p>
    <img src="../assets/vectors/landmark-down.svg" alt="" class="landmark" />
    <AudioPlayer src="text-audio/introduction.mp3" />
    <p class="subheading">Or read (1 minute)</p>
    <img src="../assets/vectors/landmark-down.svg" alt="" class="landmark" />
    <p>
      Three Systems: On Hold is a unique collaborative project and the result of
      three individual practices coming together between myself, Jo Christman
      and AJ Stillabower. I really didn’t know where this grouping would lead
      when we first met back in April 2024 for an initial chat. My diary entry
      from this day reads:
    </p>
    <p>
      We will make 3 sound boxes and my responsibility is to work out the
      narrative, what are we saying? Memory / forgetting / repeating etc.
      Thinking NHS waiting times would be a good subject matter.
    </p>
    <p>
      Throughout my musical career I’ve constantly returned to the question of
      narrative and how best to express my intentions in a piece. Questions
      similar to what are we saying are always at the forefront of my
      compositional thinking. I find that starting out with a clear intention
      around subject matter is very grounding, and often a way of making sense
      of my journey through the world through sound. I can point to most pieces
      in my back catalogue and tell you what was happening in my life around
      that time as parts of the music are likely to be self-referential, be it
      obvious or abstracted. Writing or performing music has been a safe space
      for expression for as long as I can remember but when it comes to
      collaboration, I often shy away through fear of vulnerability and the
      other people finding something out that I’d rather they didn’t. In
      collaboration with AJ and Jo, both abstract noise artists, I wondered how
      this narrative would play out.
    </p>
    <p>NHS waiting times.</p>
    <p>
      2024 posed medical challenges for my family with a lot of waiting involved
      - be that for appointments, healing, learning how to live with chronic
      pain and all the emotional baggage that goes along with it. I’ve adjusted
      to being in a carer's role during this period and my experience so far has
      involved putting pressure on myself to ensure I do everything I can to
      make my partner’s life easier as he navigates his condition. This includes
      domestic work alongside providing physical and emotional support. This is
      fine, the majority of the time, and I’ve figured out coping mechanisms for
      day-to-day jobs, but sometimes it’s just really hard to cope, resulting in
      periods of poor mental health that are durational, whole-body experiences
      which is something I’ve not really experienced before. Bouts have tended
      to start in the morning, I’ve felt them come on like a cold, then it’s a
      mad scramble to try and halt the process using internet-learned coping
      strategies: go for a walk, take deep breaths, take care of my own basic
      needs, although, deep down, I know it’s useless. That wave of stress will
      eventually take over, the problem is: I’VE STILL GOT SHIT 9 LOADS TO DO.
      HOW AM I MEANT TO GET EVERYTHING DONE MY JOB MY MASTERS MY CARING
      RESPONSIBILITIES MY PARENTS MY RELATIONSHIP WHEN THE WORLD IS AT WAR AND
      CLIMATE CHANGE ETC ETC ETC IT'S ALL JUST D O I N G M Y H E A D I N.
    </p>
    <p>(Crash land. Fuck it, I’m playing Zelda.)</p>
    <p>
      I intend to call Todmorden Medical Centre to capture a sample of the hold
      music.
    </p>
    <p>
      I got sick of hearing the hold music my partner put on speaker phone
      regularly, so did he. In 2023, The UK government spent £240 million on
      improving the phone system for when you ring your local doctors. When you
      call you are greeted by the surgery manager, in our case, Dr Bud, and you
      are told that the medical centre is dealing with calls a bit differently
      these days. You can stay on the call and enter a queue or they will call
      you back - sounds great doesn’t it, but of course, like so many public
      services in this country, the call back doesn’t always work so if you’re
      lucky to call at a time when the queue isn’t too long, you feel inclined
      to take a number and stay on the phone until it’s your time to speak to
      the receptionist. You must wait
    </p>
    <p>
      When it came to creating Three Systems: on Hold this experience as a
      subject matter seemed obvious to me, and I recorded the Todmorden Medical
      Centre hold music using a Zoom recorder - nothing fancy. I’m quite
      obsessed with the quality of phone calls and messages left on answer
      machines, and even in 2024 the quality doesn’t seem to have improved much
      from when I was younger.
    </p>
    <p>
      Conceptual artist Allan Bridge created The Apology Line which ran between
      1980 - 1995 as a phone number that people could call to disclose anything
      they felt they should apologise for. This was recorded on an answer
      machine tape. The messages span confessions of affairs, childhood secrets
      and even murder. The surreal delivery of the messages and the tape quality
      has long been of interest to me after randomly acquiring a tape release of
      the project put out by New Jersey based radio station WFMU.
    </p>
    <a
      href="https://archive.org/details/the-apology-line-uncut-gems-from-year-zero/"
      >https://archive.org/details/the-apology-line-uncut-gems-from-year-zero/</a
    >
    <!-- image -->
    <p>
      This sound quality was influential for Three Systems: On Hold, despite
      huge amounts of money being spent on the NHS phone system the music you
      hear down the phone is an imperfect and roughly put together sound. If
      you’re lucky enough to eventually hear a human answer your call, the
      waiting time means you are taken aback, oh right, I need to talk now? What
      was I calling about? Who is this person I’m revealing all my personal
      details to? Like The Apology Line, it has a confessional quality to the
      process
    </p>
    <p>
      Dante’s poem Purgatory sees its characters walking round and round Mount
      Purgatory as an allegory for the journey between hell and heaven. Waiting
      to get an NHS appointment is similar.
    </p>
    <p>
      I’ve made a 2 two-hour long loop of the medical centre recording, will it
      work? It’s pretty relentless.
    </p>
    <p>
      I was thinking about The Divine Comedy as I edited the Todmorden Medical
      Centre recording into a long loop that began with the queue number at 50
      running down to 1. Dante wouldn’t have made it that simple so to really
      exemplify that hideous and long-winded feeling I switched around the
      numbers, so they don’t go down in the way they ought to for that extra
      circle of hell energy. Originally, I kept in the standard issue music
      provided by the medical centre but when playing it back to AJ and Jo we
      decided to remove that element and replace it with their contributions
    </p>
    <p>
      The three of us brought our individual elements together at Ebor Studio in
      Littleborough where we discussed how best to visually present the work.
      The piece consists of three handmade speakers all individually linked to
      their sound sources, in my case, a cheap MP3 player, and we spent a 11
      while working out how to best position them. In the end, they were
      stylistically laid on the wooden floor of Gallery Frank surrounded by
      tangles of cables elegantly displayed to show the inner workings of the
      process.
    </p>
    <p>
      The end result is an anxiety inducing affair. Feedback builds up and
      sometimes hits a peak which occasionally, somehow produces a narrative
      line connecting the phone call, waiting, tension, stress.
    </p>
    <p>
      AJ, Jo and I spent long periods of time observing the installation in the
      space and the longer we were there, the more sense it made to me. It
      became so obvious what the work was about, the periods of silence in my
      long loop were filled with feedback and percussive noise and in that
      moment, the piece spoke of my experience of care. A rattling piece of wood
      became progressively more agitated as time went on becoming almost human
      in its reactions. I pictured clips from Lynch’s Eraserhead, slowly
      building tension in surreal restrictive spaces, I pictured myself on the
      phone getting more and more red faced and angry as the time went on. But
      is this obvious to everyone? Even now, months on, I don’t think either AJ
      or Jo had this response, I don’t know what they thought. This has been an
      interesting realisation for me, who is always trying to understand what
      people think about my work in general, is it ever truly possible?
    </p>
    <p>
      Very effective at making the point that inspired the idea so I think it
      does work. Ok.
    </p>
    <p>
      During the summer, my partner and I paid for private care treatment at a
      hospital in Leeds out of desperation to get further along in the process
      of his condition. The hospital was extremely nice, lots of parking, free
      coffee on arrival and if the appointment was running a bit late someone
      would come out to the waiting room and apologise for the delay, nothing
      like I’d experienced before. Only thing that was weird was that the radio
      in the waiting room was playing some popular radio station and the music
      kept being interfered with by loud static. My guess is the various
      scanning machines in the hospital might have been intersecting the radio
      waves, but it meant that a whole room of waiting patients were subjected
      to this wild radio station broadcasting pop tunes and waves of noise. It
      felt as though Three Systems: On Hold had somehow made its way into this
      situation and oddly, no one was reacting to it as though the sound was
      totally normal. I sat there and wondered if these people were more
      accepting than I, if faster access to healthcare made the agitation of
      noise easier to digest somehow - I’ll have whatever they’re having.
      Naturally I recorded the waiting room's static ambience for use later.
    </p>
