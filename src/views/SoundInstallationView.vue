    <img src="../assets/vectors/landmark-down.svg" alt="" class="landmark" />
    <p>
      During development for this project, each one of us had our own angle
      which we were looking to explore. Anthony was interested in listening,
      feedback and FluCoMa implemented through Supercollider, I was thinking
      about reuse and tactile interaction (exploring augmentation from
      microprocessors) and Sophie had been struggling with an uncaring medical
      system and feelings of looping. At first glance these ideas seemed
      somewhat disparate to us but as we worked through the concepts, it became
      clear that we were all exploring different presentations of a system. As
      the project developed our themes became affected by the natural gravity of
      collaboration and came together to create something in the end which I
      feel is extremely cohesive. There is a balanced tension between the
      differing systems, which at the same time bind, invite and challenge the
      listener, while forming a larger state of interaction between them. This
      idea of engendering interaction upon a semi-stable system is something
      which has come up repeatedly in my work, with previous installations
      Envision (2024), and Artefacts (2022) exploring other dimensions of this
      theme.
    </p>
    <p>
      These ideas are given a more explicit political connection in Sophie's
      third of the project, associating the binding system with her experiences
      of seeking medical treatment in our sabotaged National Health System;
      these experiences echo my own, of seeking Trans healthcare under the NHS’
      segregated GIC system and further experiences of trans identity and
      existing outside of (in opposition to) or in parallel to established
      systems.
    </p>
    <p>
      These strong feelings toward identity, boundary, agency and interaction
      weaved throughout the installation came together to create an experience
      which seeks to question and evaluate our relationship to the world from
      three very varied perspectives.
    </p>
    <p>
      For me, Boundary and agency arise from the oscillating sine tone and
      speaker rattling the piece of wood. This allows the audience to
      investigate physicality but also tease out these ideas of transition
      through an interaction of fairly simple elements which combine in
      unpredictable ways. The sound experienced by the audience is sat across a
      boundary of perception between rhythm and tone with the physical audiation
      of the wood piece giving a deeper texture, as well as a vector for control
      from the audience to sculpt the sound.
    </p>
    <p>
      The piece of wood in the speaker arose initially as a practical way to
      create more resonance from the speaker and give more definition to rhythm
      at lower frequencies. During development I found it novel to apply
      pressure to the stick and this created layers of overlapping rhythm and
      texture which introduced a simple method of audience interaction. This
      interaction is a small amount of agency over a predetermined path. A brief
      intermittence over a boundary, phasing and chaotic.
    </p>
