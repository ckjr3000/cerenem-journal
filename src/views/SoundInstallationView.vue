      experience of the building pressure of stress in medical situations and
      the way it can creep up on you without even realising it’s happening. The
      noise laps at the ears without totally immersing the listener which feels
      like a process of calm and self-regulation. For me, processing these
      sounds in this manner was a healing experience. Mapping out the sounds
